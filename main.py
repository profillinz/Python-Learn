# variable = a reusable container for storing a value
#            a variable behaves as if it were the value it contains

age = 30

 
print("I'm going to be " + str(age) + " soon") 
print("I'm going to be", age, "soon") 
print(f"I'm going to be {age} soon")

# INTEGER

Age = 30
